public class VariableAndDataType {
    public static void main(String[] args) {
        int qwertyuiop;
        qwertyuiop = 5;
        System.out.println(qwertyuiop);

        boolean b1 = true;
        boolean b0 = false;
        char c = '*';
        byte b = 127;
        short s = 128;
        int i = 54166;
        long l = 561626269L;
        float f = 12.3F;
        double d = 123.456622889622;
    }
}
